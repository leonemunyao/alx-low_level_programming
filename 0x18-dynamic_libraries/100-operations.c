#include "main.h"

/**
 * sum - calculate the sum
 * @a: integer
 * @b: integer
 * Return: Always 0
*/
int sum(int a, int b)
{
return (a + b);
}

/**
 * sub - calculates the difference
 * @a: integer
 * @b: integer
 * Return: Always 0
*/
int sub(int a, int b)
{
return (a - b);
}

/**
 * mul - calculates the product
 * @a: integer
 * @b: integer
 * Return: Always 0
*/
int mul(int a, int b)
{
return (a * b);
}

/**
 * my_div - calculates the division
 * @a: integer
 * @b: integer
 * Return: Always 0
*/
int my_div(int a, int b)
{
return (a / b);
}

/**
 * mod - calculate the mod
 * @a: integer
 * @b: integer
 * Return: Always 0
*/
int mod(int a, int b)
{
return (a % b);
}