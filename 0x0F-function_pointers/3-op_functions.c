#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
#include <stddef.h>

/**
 * op_add - adds two numbers
 * @a: int 1
 * @b: int 2
 * Return: Always 0
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtracts two numbers
 * @a: int 1
 * @b: int 2
 * Return: Always 0
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: int 1
 * @b: int 2
 * Return: Always 0
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: int 1
 * @b: int 2
 * Return: Always 0
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - find the remainder
 * @a: int 1
 * @b: int 2
 * Return: Always 0
 */

int op_mod(int a, int b)
{
return (a % b);
}
